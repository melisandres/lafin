	<div class="content">
		<h4>Francis Fullscript</h4>
		<p>Born in Rhodes Island, Fullscript studied Fine Arts at Concordia University in Montreal. He went on to exhibit in Charlottetown PEI, and Dawson City Yukon. His work deals with a hyper-personalized sense of politics, and an emotional investigation into some anarchic precepts. </p>  		
