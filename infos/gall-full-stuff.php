	<div class="content">
		<h4>artist statement</h4>
		<p>I'm grateful I din't have many things at that time in my life. If I were to make this work now... it might actually have to culminate with a move, whether that was the original intent or not.<br>Art isn't about materialism if it doesn't want to be. But there is no way to look at art in our society without seeing it through the lens of consumerism. Objects that people make.</p>
	</div>	

	<button type="button" class="collapsible">notes</button>
	<div class="content">
		<h4>residency notes:</h4>
		<p>M. Fullscript sure scratched up that moving truck. But he returned it pristine. How did he do it? I remember him calling me up in the middle of the night to complain about how hot it was, trying to sleep in there. I wasn't far, we lived in the same neighbourhood. I brought a ladder, and we climbed up to the top, set up his air mattress on the truck roof, and drank wine until the sun came up.</p>
	</div>


