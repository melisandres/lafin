		<h4>1977</h4>
		<p>In 1977, the board did not meet. James, in a desire 
		to create a virtual meeting place, created a folder
		that was sent to each board member. The folder included: 
		instructions, a topographical map drawn on acetate, 
		and an accompanying text, that read like a screenplay, 
		and was meant to be torn, as per the surviving instructions. </p>
	</div>	

	<button type="button" class="collapsible">instructions</button>
	<div class="content">
		<h4>recovered instructions:</h4>
		<p>Once out of your front door, listen attentively. If you 
		are not in anyone's way, close your eyes to listen even 
		more carefully. Turn Right, with your eyes open, with the 
		sounds etched in your consciousness.</p>
