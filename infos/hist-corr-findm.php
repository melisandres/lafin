	<div class="content">
		<h4>archivist notes</h4>
		<p>We were able to find the instructions and the map. 
		But, sadly, we were unable to find the accompanying text.
		We chose to follow the instructions, and held the fragile 
		acetate over a street map of our home. We therefor attended
		the meeting virtually, more than 40 years later. We created 
		a stand-in for the lost accompanying text: a script-like 
		document translating an imaginary argument about the nature 
		of time. We then invited today's board to join us in the 
		ceremony. Most did.</p>
