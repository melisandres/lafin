		<h4>1977</h4>
		<p>In 1977, the board did not meet. James, in a desire to create a virtual meeting place, created a folder that was sent to each board member. The folder included: instructions, an atypical cadastral map drawn on tracing paper, and an accompanying text that read like a screenplay and was to be torn as per the instructions. We have not been able to find any surviving copies of the accompanying text. </p>
	</div>	

