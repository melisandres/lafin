		<h4>Château Eustache</h4>
		<p>If you are ever invited to the Mayfield or Ruggenbaum
		wine cellars, these aren't the first bottles you'll notice.
		They live in a few cases, at the back. They don't get much
		airplay. They haven't aged gracefully, but not terribly 
		either. I'm told they are kept as a kind of existential
		medecine&mdash;a reminder that sometimes things just happen, 
		out in the dispassionate real world, that mirror something 
		internal and profound. In these rare moments, it really feels like 
		there's an intelligence peering through nature and construct,
		into our souls, and tracing out a sign&mdash;in this case, a 
		wine sign. </p>
