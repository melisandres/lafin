		<h4>Les Paquets</h4>
		<p>For over three generations, the Paquets have been preparing and 
		distributing wine for special requests&mdash;<i>des vins d'occasion</i>.
		The Paquet winery has specialized in this adaptability, preparing
		and bottling for partners outside of the industry, and preparing, 
		at their leisure, a simple "vin de table," which they do not sell 
		commercially.</p>
