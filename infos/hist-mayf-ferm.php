		<p>Our 1986 Château Eustache delivers a delicate aroma of 
		crushed sour cherry and wild grape. Its base notes of 
		hazelnuts and crackling kindling are reminiscent of the 
		summer that inspired its creation. With no other 
		vintages&mdash;only one year, one edition&mdash;it was 
		christened after an imagined castle set aflame by Eustace, 
		patron saint of fire-fighters and butt of a running joke 
		on poisonous patronage. We found the field that yielded 
		its grapes in smoldering cinders the day after we picked 
		the vines clean, so we gathered whisps of smoke to add the 
		the barrels.</p>
		<p>Produced at the Paquets' family-owned winery nestled 
		in the French Alps, our pillaged field of wild grapes 
		yielded three barrels. Dispensed by the spoon or glassful,
	 	this elegant pastiche pairs well with critical cases of 
		nostalgia and similar ailments of the soul.</p>
	
