	<p>The <i>cadavre exquis</i>, was a method elaborated by the surrealists, to produce collaborative work. The participants, only experiencing a fragment of the work they are adding to, expand on that fragment before hiding everything but the edge of their addition. Members of the Board began such a series in the early 80's. The experiment continued for years.</p>
    <p>
    A stack of collaborative pieces, forgotten, were found in Beauregard's posession after he passed away. The pieces were found altered, almost unrecognizable. Although this may be due to faults in the memories of those who contributed, most surving board members believe that Beauregard continued working on these pieces, even after their existance had faded from the collective memory, furthering the surrealist desire to use the exquisite corpse to connect to a kind of jungian collective unconscious.</p>
