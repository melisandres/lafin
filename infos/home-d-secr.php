	<div class="content">
		<h4>Welcome to our homes : local restrictions apply</h4>
		<p>
		We've long ago&mdash;long before the Covid 19 pandemic&mdash;given
		up on the idea that our private and professional lives might 
		somehow exist independantly. In many ways, the institute is
		an outgrowth of both. In the uncertainty of a global 
		pandemic, and in the sometimes dubious safety of isolation, 
		we felt it important to acknowledge how lucky we are to 
		exist in a fiction-based paradigm, not white or blue 
		collar, but self-determined/wear what you want/work in your 
		flannel. Not essential or non-essential, but, in essence, 
		continuing the work we chose. This, we acknowledge, is 
		a priviledge afforded to few.
		</p>
	</div>

