	<p>In these times, we thought it might be <i>fun/not fun</i> to explore the rich universe of the lie. What is a lie? How many lies can one reality sustain before it starts collapsing on itself? A world in which lies dominate becomes a mind boggling paradox, but where is the comfortable lie? The one we cozy up to, the one that won't disrupt anything? We hope that charting the realm of lies might help clarify fiction, as well as truth.</p>
