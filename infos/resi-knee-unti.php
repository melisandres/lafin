		<h4>a three curtain play</h4>
		<p>Behind the curtain, the draw-string hangs out of the window and interacts with the world outside. The audience stands around the closed curtain until... the show starts.</p>
		<h4>Curtain One: The Left Room faces Gottingen street</h4>
		<p>The drawstring is attached to a car antennae. The car is parked in front of the downstairs local hang-out/convenience store. The car owner comes out of the store yelling to others. He pulls the drawstring, looking into the window at the audience from below.</p>
		<h4>Curtain Two: The Middle Room faces the alleyway</h4>
		<p>The drawstring is attache to a cinderblock. A stranger runs for their life, into the alley, trips on the drawstring, hurriedly gets up, looks back, and continues running into the darkness.</p>
		<h4>Curtain Three: The kitchen faces Joe's roof</h4>
		<p>A man is standing on the roof, two feet from the window. Holding the drawstring in one hand and his trumpet in the other, he pulls the drawstring. A spotlight turns on. He begins playing his trumpet, level to and facing the audience, his trumpet clinking against the window.</p>
