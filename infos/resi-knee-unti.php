	<button type="button" class="collapsible">artist statement</button>
	<div class="content">
		<p>When I make a suit, I see a body in it, before and after it is.</p>
		<p>Suits and props, and a stage. Environments are built around missing pieces... they are built around the repeated conjuring of an absent subject. I am interested in the act of conjuring, the attributes and idealism associated to the conjured, subject... or environment.</p>
		<p>My world is build for ghosts. The connection between idealism, imagination, and hand-made environments is where my activities lie.</p>
	</div>

