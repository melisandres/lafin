		<p> In the winter of 2000, <i>Postmodern Deconstruction of Death in the Hyperreal</i> was a one night event that brought together two artists/lecturers visiting from New York. Anita Beaumont and Frank Mayfield spent a week in Halifax, each preparing a body of work designed to culminate in a shared lecture/presentation.</p>
		<p>The event was open to the public and presented in the Bell Auditorium, at the Nova Scotia College of Art and Design, Halifax, NS.</p>
