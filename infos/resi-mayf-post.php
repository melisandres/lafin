		<h4>Frank Mayfield</h4>
		<p>Based in Chicago, Frank Mayfield divides her time between theoretical research, art making, and curatorial work. Her practice demonstrates her particular interest in the uncanny. Her methods outline experimental approaches to scientific research, placing a particular emphasis on a structural deconstruction of methodology and quantitative abnegation.</p>

		<p>Frank has published extensively on the theoretical links between death, science, and contemporary art making. Over the last 20  years she has curated for, and shown work in, galleries across Europe, North America, and Asia.</p>

		<p>Over the last 5 years, Frank has been the Executive Director of the Centre de Recherches et d’Etudes Nouvelles. She commutes between France and Chicago, from where she oversees Lafin’s research department. </p>  		
