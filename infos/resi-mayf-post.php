Anita becomes impatient. The faculty liaison steps up, apologizes for time constraints and presents Anita.



Anita Beaumont 

Anita approached directly the work she produced on the occasion of her visit to Halifax. Revivification being a

central concern within her work and her life, Anita shows slides of an ambulance, and speaks of the phenomena of being

pulled back from death. The question that was of interest to her during her time in Halifax: “How many artists can one

revive in an hour?”. 



Slides are listed off, showing various members of the Halifax art community, who had been asked a week prior to

pause in front of the camera, wearing certain articles of clothing, smiling, and looking happy to be alive. 



These individuals were not explained the nature of the project, but were however urged to attend the upcoming lecture.

At each of these slides, Anita talks about visual concerns and aesthetics, while she restores the fingers of revived artists

to their rightful owner, or someone willing to pass it on to the absent artist.



(performing: Tania Sures and Mélisandre Schofield) </p>



