		<h4>naming things after</h4>
		<p>For a period of some years, in a way that is not so unusual, I had a floor of my own. Still, as it was not always the case, I felt particularly lucky when that floor had a length and a width that could accomodate a small matt where a guest could sleep.</p>
		<p>It wasn't until later that the phenomena of clearing out a space on the floor, and tiptoeing around a sleeping body in order to make a morning coffee was given a name. And by this time, the floor was a room, and the matt was a bed, and <i>the middle room residency</i> was born. But until then, guests were not called residents, and the work they made in the hours that the matt was a vacant heap of sheets and blanket-like clothing was just the stuff of life&mdash;nothing more, nothing less.</p>
