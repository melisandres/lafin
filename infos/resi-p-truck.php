	<p>between October 1999 and January 2001, the residency lived in a truck--a moving truck.</p>
	</div>

	<button type="button" class="collapsible">statement</button>
	<div class="content">
		<h4>three things to know about the moving truck:</h4>
		<p>Although it represented a period of upheaval as Mé moved from one place to another, it was never used to move Mé, at least not in the ways one might expect.</p>
		<p>It was sometimes there, sometimes not, sometimes not quite itself.</p>
		<p>It was not exactly real, nor was it exactly a figment of anyone's imagination.</p>
	</div>	

	<button type="button" class="collapsible">notes</button>
	<div class="content">
		<h4>remarks from the residency host:</h4>
		<p>I've always found the intimacy of studio clutter more inviting than the cool white of a gallery wall. What interests me in art, is the layering of actions, questions, the way process creates soft accumulations, landscapes of questions, attempts, and incompleteness. For me, the experience deepens when a piece reveals its path, its struggle. I like to think of hosting as a way to mirror this emphasis on process, especially the kind of hosting that lasts for weeks, and slips its way into the every day, those details that weave together to create the armature of life. Relocating the residency to the moving truck, although necessary, felt sad. Not only did it create a distance between the residency studio and my own, but it took away the shared breakfast, the quick breaks in the kitchen, sharing snacks, sharing quips across one studio to the next.</p>
	</div>

