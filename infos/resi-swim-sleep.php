	<div class="content">
		<p>I don't think there are audiences; I think there are just witnesses, snoops, and small communities who witness and snoop on each other.</p>
		<p>The further isolating our society becomes, the more forbidden our narratives. We are pocketed into distinct categories. Within our demographic ranges there are specifically manufactured narratives to keep us occupied.</p>
		<p>In targeting market audiences, and pushing for the creation of work that treats people as demographics, today's world of publishing is in the trench digging business. Narratives are social unifiers. As the focus and target, they demarcate territories like conquering nations. This is also the case with children and young adult literature. Generational gaps are easily aggravated by the formation of entirely different narrative cultures.</p>
		<p>I'm in the business of forbidden narratives. The kinds of narratives that travel without passports.</p>
	</div>

