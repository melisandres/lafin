		<h4>More about<i>Sleeping with a Monster in the Closet</i></h4>
		<p>I wanted to write something non-generational by using some the the structures that inform the typical children's story, and then slipping in difficult imagery, a dubious narrative flow, and a premise that didn't give a clear moral framework.</p>
		<p>I remember story time as a child, waiting to hear the same story over and over again. I wanted to rebuild that experience, of not being able to remember what comes next... despite having just heard it.</p>
		<p>An experience similar to trying to remember a dream. When I wake-up from a dream, the atmosphere is the only thing that remains crystal clear, and from that atmosphere, I can try to remember the dream, but often find I'm not sure if I'm remembering the dream, or just making parts of it up as I go along. Retelling a dream is reinventing it... dream data is malleable because it comes from a mental state rather than creates it.</p>
		<p>In a similar way, to read a narrative that breaks down cause and effect into a general state of mind, creates a space that calls for reinventing the text continually, instead of remembering the text.</p>
