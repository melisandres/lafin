	<div class="content">
		<h4>Jeremy Swimmer</h4>
			<p>With no formal training, and few preconceptions, Jeremy Swimmer's work stands far from that of the mainstream in cross-generational literature. Jeremy's focus on the role of narrative and the book object within social frameworks pushes him into strange literary backroads.</p>
			<p>Founder and editor of <i>Catch-a-Cold-Press</i>, a do-it-yourself press in the basement of his family's Annapolis Royal summer home. Jeremy publishes short-run editions, in hand crafted binding, with scotch tape, staples, and more refined processes, such as stitch bindings, and hard-cover editions.</p>  
			<p>Jeremy's work spans text, illustration, and the book object. In addition to his expansive collection of self published chap books, Jeremy has published for magazines and small presses in Nova Scotia, New Brunswick, and Montreal.</p>
			<p>***By night, Jeremy works as a security guard at NSCAD University, where studio hallways have given him the alternative education of an intrepid night student.</p>  		  				
