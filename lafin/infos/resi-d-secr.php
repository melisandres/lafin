		<p>We believe that art and life are cut from the same shivering cloth.</p>
		<p>Although attempts at an artist residency program were made in the first half of the twentieth century, the fog of institutional memory only begins to clear around the mid 1970's. In 1976 board members Frank Mayfield and Stacey Ruggenbaum created an informal opportunity for artists to spend a season with them on their boat.</p> 
		<p>Since about 2003, Lafin's residency has been tucked under board member MÃ©lisandre Schofield's arm. Imaginary when it must be, and real when it can be, the residency benefits from its ability to travel and adapt to the lives it touches. It offers everything it has, even when it has nothing. At times it has been a bed in a room, a couch on a roof, the bed of a truck, a slanted apartment, a sliver of boat. But most often it has been an abstract idea, fiercly independent from the financial realities that would crush it,  reshaping itself to the needs of its guests and hosts.</p>
