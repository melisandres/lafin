		<p>While a resident at Lafin, Millefeuilles rummaged through the apartment, and set off on adventures through the streets of Halifax. His goal was to create and collect simple moments, moments he could document later, through drawings and collages. As his journal filled, it became clear to him that these moments were not to be presented in isolation, but rather as sister moments. He grouped them, so that they might imbue each other with meaning. He grouped them by day, but these temporal links developed and mutated into other kinds of pairings, triplings&mdash;connections that might allow a world of possibilities to emmerge between them.</p>
