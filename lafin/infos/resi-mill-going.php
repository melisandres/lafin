	<div class="content">
		<h4>Millefeuilles</h4>
		<p>Millefeuilles is a pastry. Born in Vienna, probably&mdash;or else France.</p> 
		<p>An artist name chosen for its layers and its messy eating, Millefeuilles, born Isaac Walker, feels the true nature of the artist is dionysian, and must issue from a chaotic and full-bodied engagement with its subject, which should ultimately be life itself: from the delicate and refined to the one pot mash. Every aspect of experience needs to be crushed, crumpled, and squeezing out at the sides while it's still fresh. Every well-constructed argument needs to be pounded with the weight of corporeality, enjoyed with decadance, not as a thing that will remain over generations, but as a thing that has been carefully crafted for the shortest of shelf-lives, which is the true staying-power of most precious things.</p>		
