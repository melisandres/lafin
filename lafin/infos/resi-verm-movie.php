	<div class="content">
	<p>For the length of Jodie Vermeer's stay at the middle room residency, she watched movies, and made rudimentary reproductions of artwork featured in the background of those movies. Her work was not only to reproduce the art she found particularly loathsome, but to pontificate about the circumstances she imagined led to their inclusion in the movie.</p>
	<p><i>"Art included in a movie is often art created specifically for that movie. It benefits from a narrow context. To anyone who knows about the art world, it looks like something made by a novice--probably the director, or his brother-in-law."</i></p>
	</div>

	<button type="button" class="collapsible">artist statement</button>
	<div class="content">
		<p>I choose to watch movies, not for the plot, not for the feels, but for the recursive aspect of art looking at art. How does the art, the works that run through like mute characters within the film, reflect something about the piece as a whole. More than a character, rather a microcosm, a world within a world, art within art. </p>
	</div>

